        def getTextKey

            json = {
                "type": "text"
            }
            return json
        end
        
        def getBtnKey(*arg)

            json = {
              "type": "buttons",
                "buttons": []
            }

            arg.each do |a|
                json[:buttons] = a
            end

            return json

        end


